#include "dog.h"
#include <stdio.h>
#include <stdlib.h>
/**
 * new_dog - this funcion prints dog nfo
 * @name: dog name
 * @age: dog age
 * @owner: dog owner
 * dog - struct name
 */
dog_t *new_dog(char *name, float age, char *owner)
{
	if (new_dog == NULL)
		return;
	new_dog->name = name;
	new_dog->age = age;
	new_dog->owner = owner;
}
